#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

#define RESET "\033[0m"
#define RED "\033[31m"

static void	toContinue(void) {
	std::cout <<  RED << "Press ENTER to continue...\n\n" << RESET;
	std::getchar();
}

int main() {
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	toContinue();
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.attack();
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return 0;
}
